for alpha in range (1, 51): #variable named alpha declares a range
    print (alpha)

for beta in reversed(range(1, 51)): #variable named beta declares a reversed range
    print (beta) 

for charlie in range (30, 51): #variable named charlie declares a range
    print (charlie) 

for delta in reversed(range(10, 51, 2)): #variable named delta declares reversed range
    print (delta) #print with decrements of two

for echo in range (100, 201, 5): #variable named echo declares a range
    print (echo) #prints with increments of five